import streamlit as st

st.set_page_config(page_title="البرنامج | الشباب يقدر", page_icon="../assets/favicon.png", layout="wide")

st.markdown("## البرنامج الانتخابي")
st.write(
    "يعتمد البرنامج على أولويات قريبة الأثر ومتوسطة المدى، مع مصفوفة مؤشرات أداء معلنة "
    "وشراكات مع الجهات التنفيذية والمجتمع المدني."
)

pillars = [
    ("الخدمات الأساسية", "رفع كفاءة الخدمات البلدية والصحية والتعليمية."),
    ("البنية التحتية", "تحسين الطرق والإنارة والصرف وتوسيع الشبكات الذكية."),
    ("تمكين الشباب", "تدريب، توظيف، ريادة أعمال، دعم المبادرات."),
    ("دعم المرأة", "تمكين المشاركة الاقتصادية والاجتماعية."),
    ("الشفافية", "منصّات متابعة المشاريع ونشر التقارير."),
    ("التنمية المحلية", "استثمار مجتمعي وسياحة صغيرة وحرف."),
]

cols = st.columns(3, gap="large")
for i, (t, d) in enumerate(pillars):
    with cols[i % 3]:
        st.markdown(f"### {t}")
        st.write(d)

with st.expander("تفاصيل إضافية (Placeholder)"):
    st.write("أدرج هنا الوثائق التفصيلية وروابط المتابعة والخرائط التفاعلية لاحقًا.")
